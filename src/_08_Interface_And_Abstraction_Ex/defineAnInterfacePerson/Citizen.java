package _08_Interface_And_Abstraction_Ex.defineAnInterfacePerson;

public class Citizen implements Person{

 private String name;
    private int age;

    public Citizen(String name, int age) {
        this.name = name;
        this.age = age;
    }

    @Override
    public String getName() {
        return this.name;
    }

    @Override
    public int getAge() {
        return this.age;
    }
}
